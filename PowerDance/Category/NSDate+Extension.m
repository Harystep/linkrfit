+ (NSString *)reduceMonthFormDate:(NSString *)currentDate {
    
    NSDate *dates = [NSDate date:currentDate WithFormat:@"yyyy-MM"];
    NSCalendar *calendar = [[NSCalendar alloc] initWithCalendarIdentifier:NSCalendarIdentifierGregorian];
    NSDateComponents *lastMonthComps = [[NSDateComponents alloc] init];
    [lastMonthComps setMonth:-1];
    NSDate *newdate = [calendar dateByAddingComponents:lastMonthComps toDate:dates options:0];
    NSString *dateStr = [newdate stringWithFormat:@"yyyy-MM"];
    return dateStr;
}

